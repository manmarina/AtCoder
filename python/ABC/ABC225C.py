N, M = map(int, input().split())
A = [list(map(int, input().split())) for _ in range(N)]

# 最上行が週をまたがないか（行頭の曜日 + (M-1) <= 6）
if (A[0][0] - 1) % 7 + (M - 1) > 6:  # <- この条件の実装が見事！！
    print("No")
    exit()

# 横方向チェック（連続 + 同一週内は上述の条件で担保済み）
for i in range(N):
    for j in range(M - 1):
        if A[i][j + 1] != A[i][j] + 1:
            print("No")
            exit()

# 縦方向チェック（+7）
for i in range(N - 1):
    for j in range(M):
        if A[i + 1][j] != A[i][j] + 7:
            print("No")
            exit()

print("Yes")

