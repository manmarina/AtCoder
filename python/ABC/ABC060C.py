N, T = map(int, input().split())
t = list(map(int, input().split()))

water = [T]
for i in range(1, N):
    d = t[i] - t[i - 1]  # 前回ボタンを押してからの経過時間
    if d < T:  # 経過時間がお湯の出る長さより短い時
        water[i - 1] = d  # 前回の湯量を減らす
    water.append(T)

# print(water)
print(sum(water))


